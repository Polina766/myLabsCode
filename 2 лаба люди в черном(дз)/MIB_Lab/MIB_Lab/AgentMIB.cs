using System;
using System.IO;

namespace MIB_Lab
{
    // SEALED КЛАСС - от этого класса НЕЛЬЗЯ наследоваться дальше
    // Это "конечная" ветка иерархии
    // Наследуется от Гуманоида, подключает интерфейс IОружие
    sealed class АгентMIB : Гуманоид, IОружие
    {
        public string букваАгента;
        public string ранг;

        public const string ПАРОЛЬ_MIB = "ЧЕРНЫЙ_КОСТЮМ";
        public static int всегоАгентов = 0;

        // КОНСТРУКТОР ПО УМОЛЧАНИЮ
        public АгентMIB() : base()
        {
            Random генератор = new Random();
            string[] буквы = { "K", "J", "M", "O", "L", "X", "Z", "T", "D", "F" };
            букваАгента = буквы[генератор.Next(буквы.Length)];
            ранг = "стажер";
            всегоАгентов++;
        }

        // КОНСТРУКТОР С ПАРАМЕТРАМИ
        public АгентMIB(string планета_п, int опасность_п, double здоровье_п,
                        int пальцы_п, string профессия_п, string буква_п, string ранг_п)
            : base(планета_п, опасность_п, здоровье_п, пальцы_п, профессия_п)
        {
            букваАгента = буква_п;
            ранг = ранг_п;
            всегоАгентов++;
        }

        // SEALED МЕТОД - нельзя переопределить в потомках (но класс sealed, так что потомков нет)
        // Переопределяем виртуальный метод из Гуманоида
        public sealed override void Замаскироваться()
        {
            Console.WriteLine($"АГЕНТ {букваАгента} (ранг: {ранг}) В ЧЕРНОМ КОСТЮМЕ!");
        }

        // СТАТИЧЕСКИЙ МЕТОД - можно вызвать без создания объекта
        public static void ВызватьПодкрепление()
        {
            Console.WriteLine("ВНИМАНИЕ! ВЫЗВАН ОТРЯД MIB!");
            Console.WriteLine($"   Всего агентов на дежурстве: {всегоАгентов}");
            Console.WriteLine($"   Пароль: {ПАРОЛЬ_MIB}");
        }

        // УНИКАЛЬНЫЙ МЕТОД агента - нейтрализатор памяти
        public void ИспользоватьНейтрализатор(Инопланетянин цель)
        {
            Console.WriteLine($"*ЯРКАЯ ВСПЫШКА* Агент {букваАгента} использует нейтрализатор!");
            Console.WriteLine($"*звук: ВЖЖЖЖ-ЩЕЛК*");
            Console.WriteLine($"Существо с планеты {цель.планета} забыло, что оно инопланетянин!");
            цель.уровеньОпасности = 1; // становится безопасным
            цель.здоровье /= 2; // здоровье уменьшается вдвое
        }

        // РЕАЛИЗАЦИЯ метода интерфейса IОружие
        public void Нейтрализовать(Инопланетянин цель)
        {
            Console.WriteLine($"Агент {букваАгента} стреляет!");
            цель.здоровье = 0;// полная нейтрализация
            Console.WriteLine($"Цель нейтрализована. Пришелец с планеты {цель.планета} повержен.");
        }

        // ПЕРЕОПРЕДЕЛЕНИЕ вывода на экран
        public override void ВывестиНаЭкран()
        {
            Console.Write("*** АГЕНТ MIB *** ");
            base.ВывестиНаЭкран();
            Console.WriteLine($"   Буква агента: {букваАгента}, Ранг: {ранг}");
        }

        // РЕАЛИЗАЦИЯ абстрактного метода
        public override void ИздатьЗвук()
        {
            Console.WriteLine($"Агент {букваАгента}: Я люблю эту работу! *щелкает нейтрализатором*");
        }
    }
}