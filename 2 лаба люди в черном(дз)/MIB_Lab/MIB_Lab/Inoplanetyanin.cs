using System;
using System.IO;

namespace MIB_Lab
{
    abstract class Инопланетянин // АБСТРАКТНЫЙ КЛАСС - нельзя создать объект этого класса напрямую
    {
        public string планета;
        public int уровеньОпасности;
        public double здоровье;

        public static int всегоСуществ; // Хранит общее количество созданных существ

        // КОНСТРУКТОР ПО УМОЛЧАНИЮ
        public Инопланетянин()
        {
            Random генератор = new Random();
            string[] названияПланет = { "Марс", "Венера", "Ксерон", "Арктур", "Зета" };
            планета = названияПланет[генератор.Next(названияПланет.Length)];
            уровеньОпасности = генератор.Next(1, 11);
            здоровье = генератор.Next(50, 201);
            всегоСуществ++;
        }

        // КОНСТРУКТОР С ПАРАМЕТРАМИ
        public Инопланетянин(string планета_п, int опасность_п, double здоровье_п)
        {
            планета = планета_п;
            уровеньОпасности = опасность_п;
            здоровье = здоровье_п;
            всегоСуществ++;
        }

        // КОНСТРУКТОР КОПИРОВАНИЯ
        public Инопланетянин(Инопланетянин другой)
        {
            планета = другой.планета;
            уровеньОпасности = другой.уровеньОпасности;
            здоровье = другой.здоровье;
            всегоСуществ++;
        }

        // СТАТИЧЕСКИЙ МЕТОД - можно вызвать без создания объекта
        public static void ПоказатьСтатистику()
        {
            Console.WriteLine($"Всего существ создано: {всегоСуществ}");
        }

        // ВИРТУАЛЬНЫЙ МЕТОД - может быть ПЕРЕОПРЕДЕЛЕН в классах-наследниках
        public virtual void ВывестиНаЭкран()
        {
            Console.WriteLine($"Планета: {планета}, Опасность: {уровеньОпасности}, Здоровье: {здоровье}");
        }

        // ВИРТУАЛЬНЫЙ МЕТОД для ввода данных с клавиатуры
        public virtual void ВводСКлавиатуры()
        {
            Console.WriteLine("Введите название планеты:");
            планета = Console.ReadLine();

            do
            {
                Console.WriteLine("Введите уровень опасности (от 1 до 10):");
            } while (!int.TryParse(Console.ReadLine(), out уровеньОпасности) ||
                     уровеньОпасности < 1 || уровеньОпасности > 10);

            do
            {
                Console.WriteLine("Введите здоровье (больше 0):");
            } while (!double.TryParse(Console.ReadLine(), out здоровье) || здоровье <= 0); // TryParse пытается преобразовать строку в число
        }

        public virtual void ЗаписьВФайл(StreamWriter поток)// ВИРТУАЛЬНЫЙ МЕТОД для записи в файл
        {
            поток.WriteLine($"{планета},{уровеньОпасности},{здоровье}");
        }

        // АБСТРАКТНЫЙ МЕТОД - не имеет реализации в базовом классе
        // ВСЕ наследники ОБЯЗАНЫ его реализовать (переопределить)
        public abstract void ИздатьЗвук();
    }
}