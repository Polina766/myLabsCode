namespace MIB_Lab
{
    class General
    {
        static void Main()
        {
            Console.WriteLine("╔══════════════════════════════════════════════════════════╗");
            Console.WriteLine("║              ЛЮДИ В ЧЕРНОМ: СЕКРЕТНЫЕ ФАЙЛЫ              ║");
            Console.WriteLine("╚══════════════════════════════════════════════════════════╝");
            Console.WriteLine();

            // СОЗДАЕМ МАССИВЫ
            Инопланетянин[] всеСущества = new Инопланетянин[8];
            IМаскировка[] маскировщики = new IМаскировка[4];
            IОружие[] оружие = new IОружие[4];

            int индексМаскировки = 0;
            int индексОружия = 0;

            Console.WriteLine("───────────── СОЗДАНИЕ СУЩЕСТВ ─────────────");

            // Гуманоиды
            всеСущества[0] = new Гуманоид();
            Console.WriteLine("  [+] Создан случайный гуманоид");

            всеСущества[1] = new Гуманоид("Марс", 3, 150, 5, "продавец");
            Console.WriteLine("  [+] Создан гуманоид с параметрами");

            // Арахниды
            всеСущества[2] = new Арахнид();
            Console.WriteLine("  [+] Создан случайный арахнид");

            всеСущества[3] = new Арахнид("Ксерон", 8, 200, 8, true);
            Console.WriteLine("  [+] Создан ядовитый арахнид");

            // Агенты MIB
            всеСущества[4] = new АгентMIB();
            Console.WriteLine("  [+] Создан случайный агент MIB");

            всеСущества[5] = new АгентMIB("Земля", 2, 180, 5, "агент", "K", "старший агент");
            Console.WriteLine("  [+] Создан АГЕНТ K (легенда MIB)");

            // Копия и еще один
            всеСущества[6] = new Гуманоид((Гуманоид)всеСущества[1]);
            Console.WriteLine("  [+] Создана копия гуманоида");

            всеСущества[7] = new Арахнид();
            Console.WriteLine("  [+] Создан еще один арахнид");
            Console.WriteLine("────────────────────────────────────────────");

            // Заполняем интерфейсные массивы
            for (int i = 0; i < всеСущества.Length; i++)
            {
                if (всеСущества[i] is IМаскировка маск && индексМаскировки < маскировщики.Length)
                {
                    маскировщики[индексМаскировки] = маск;
                    индексМаскировки++;
                }

                if (всеСущества[i] is IОружие ор && индексОружия < оружие.Length)
                {
                    оружие[индексОружия] = ор;
                    индексОружия++;
                }
            }

            // ========== ЗАДАНИЕ 1: ВЫВОД ВСЕХ ==========
            Console.WriteLine("\n───────────── ЗАДАНИЕ 1: ВСЕ СУЩЕСТВА ─────────────");
            for (int i = 0; i < всеСущества.Length; i++)
            {
                Console.Write($"  [{i}] ");
                всеСущества[i].ВывестиНаЭкран();
            }
            Console.WriteLine("────────────────────────────────────────────────────");

            // ========== ЗАДАНИЕ 2: ЗВУКИ ==========
            Console.WriteLine("\n───────────── ЗАДАНИЕ 2: КТО КАК ЗВУЧИТ ────────────");
            for (int i = 0; i < всеСущества.Length; i++)
            {
                Console.Write($"  {всеСущества[i].GetType().Name,-12}: ");
                всеСущества[i].ИздатьЗвук();
            }
            Console.WriteLine("────────────────────────────────────────────────────");

            // ========== ЗАДАНИЕ 3: МАСКИРОВКА ==========
            Console.WriteLine("\n───────────── ЗАДАНИЕ 3: МАСКИРОВКА ─────────────────");
            for (int i = 0; i < маскировщики.Length; i++)
            {
                if (маскировщики[i] != null)
                {
                    Console.Write($"  Маскировщик #{i}: ");
                    маскировщики[i].Замаскироваться();
                }
            }
            Console.WriteLine("────────────────────────────────────────────────────");

            // ========== ЗАДАНИЕ 4: ОРУЖИЕ ==========
            Console.WriteLine("\n───────────── ЗАДАНИЕ 4: ПРИМЕНЕНИЕ ОРУЖИЯ ─────────");
            if (оружие[2] != null && всеСущества[2] != null)
            {
                Console.WriteLine("  >> Первое оружие атакует арахнида!");
                оружие[2].Нейтрализовать(всеСущества[2]);
            }
            Console.WriteLine("────────────────────────────────────────────────────");

            // ========== ЗАДАНИЕ 5: УНИКАЛЬНЫЕ МЕТОДЫ ==========
            Console.WriteLine("\n───────────── ЗАДАНИЕ 5: УНИКАЛЬНЫЕ МЕТОДЫ ──────────");
            for (int i = 0; i < всеСущества.Length; i++)
            {
                if (всеСущества[i] is Гуманоид гум)
                {
                    Console.Write($"  Гуманоид #{i}: ");
                    гум.НайтиРаботу("космический таксист");
                }
                if (всеСущества[i] is Арахнид арах)
                {
                    Console.Write($"  Арахнид #{i}: ");
                    арах.ОтложитьЯйца();
                }
                if (всеСущества[i] is АгентMIB агент)
                {
                    Console.Write($"  Агент #{i}: ");
                    агент.ИспользоватьНейтрализатор(всеСущества[0]);
                }
            }
            Console.WriteLine("────────────────────────────────────────────────────");

            // ========== ЗАДАНИЕ 6: СТАТИКА И КОНСТАНТЫ ==========
            Console.WriteLine("\n───────────── ЗАДАНИЕ 6: СТАТИСТИКА MIB ─────────────");
            Инопланетянин.ПоказатьСтатистику();
            Console.WriteLine($"  Пароль MIB: {АгентMIB.ПАРОЛЬ_MIB}");
            АгентMIB.ВызватьПодкрепление();
            Console.WriteLine("────────────────────────────────────────────────────");

            // ========== ЗАДАНИЕ 7: ПРОВЕРКА ТИПОВ ==========
            Console.WriteLine("\n───────────── ЗАДАНИЕ 7: ОПРЕДЕЛЕНИЕ ТИПОВ ──────────");
            for (int i = 0; i < всеСущества.Length; i++)
            {
                if (всеСущества[i] is АгентMIB агент)
                {
                    Console.WriteLine($"  [АГЕНТ] Существо [{i}] - АГЕНТ {агент.букваАгента} (MIB!)");
                }
                else if (всеСущества[i] is Гуманоид)
                {
                    Console.WriteLine($"  [ГУМ] Существо [{i}] - ГУМАНОИД");
                }
                else if (всеСущества[i] is Арахнид)
                {
                    Console.WriteLine($"  [АРАХ] Существо [{i}] - АРАХНИД");
                }
            }
            Console.WriteLine("────────────────────────────────────────────────────");

            // ========== ЗАДАНИЕ 8: САМОЕ ОПАСНОЕ ==========
            Console.WriteLine("\n───────────── ЗАДАНИЕ 8: САМОЕ ОПАСНОЕ ──────────────");
            Инопланетянин самыйОпасный = всеСущества[0];
            for (int i = 1; i < всеСущества.Length; i++)
            {
                if (всеСущества[i].уровеньОпасности > самыйОпасный.уровеньОпасности)
                {
                    самыйОпасный = всеСущества[i];
                }
            }
            Console.WriteLine("  !! САМОЕ ОПАСНОЕ СУЩЕСТВО !!");
            Console.Write("  ");
            самыйОпасный.ВывестиНаЭкран();
            Console.WriteLine("────────────────────────────────────────────────────");

            // ========== ЗАДАНИЕ 9: ЗАПИСЬ В ФАЙЛ ==========
            Console.WriteLine("\n───────────── ЗАДАНИЕ 9: ЗАПИСЬ В ФАЙЛ ──────────────");
            try
            {
                using (StreamWriter файл = new StreamWriter("существа.txt"))
                {
                    for (int i = 0; i < всеСущества.Length; i++)
                    {
                        всеСущества[i].ЗаписьВФайл(файл);
                    }
                }
                Console.WriteLine("  [OK] Данные сохранены в файл 'существа.txt'");
            }
            catch (Exception ошибка)
            {
                Console.WriteLine($"  [X] Ошибка: {ошибка.Message}");
            }
            Console.WriteLine("────────────────────────────────────────────────────");

            // ========== ЗАДАНИЕ 10: ВВОД С КЛАВИАТУРЫ ==========
            Console.WriteLine("\n───────────── ЗАДАНИЕ 10: СОЗДАНИЕ НОВОГО ───────────");
            Console.WriteLine("  Хотите создать новое существо? (да/нет)");
            Console.Write("> ");

            if (Console.ReadLine().ToLower() == "да")
            {
                Console.WriteLine("\n  ВЫБЕРИТЕ ТИП:");
                Console.WriteLine("    1 - Гуманоид");
                Console.WriteLine("    2 - Арахнид");
                Console.WriteLine("    3 - Агент MIB");
                Console.Write("  > ");

                int выбор = int.Parse(Console.ReadLine());
                Инопланетянин новоеСущество = null;

                switch (выбор)
                {
                    case 1:
                        новоеСущество = new Гуманоид();
                        Console.WriteLine("\n  СОЗДАНИЕ ГУМАНОИДА:");
                        break;
                    case 2:
                        новоеСущество = new Арахнид();
                        Console.WriteLine("\n  СОЗДАНИЕ АРАХНИДА:");
                        break;
                    case 3:
                        новоеСущество = new АгентMIB();
                        Console.WriteLine("\n  СОЗДАНИЕ АГЕНТА MIB:");
                        break;
                    default:
                        Console.WriteLine("  [X] Неверный выбор!");
                        break;
                }

                if (новоеСущество != null)
                {
                    Console.WriteLine("  Введите данные:");
                    Console.WriteLine("  " + new string('─', 40));
                    новоеСущество.ВводСКлавиатуры();

                    Console.WriteLine("\n  НОВОЕ СУЩЕСТВО СОЗДАНО:");
                    Console.Write("  ");
                    новоеСущество.ВывестиНаЭкран();
                }
            }
            Console.WriteLine("────────────────────────────────────────────────────");

            // ========== ФИНАЛ ==========
            Console.WriteLine("\n══════════════════════════════════════════════════════════");
            Console.WriteLine("     УРРРРАААА! ЛАБОРАТОРНАЯ РАБОТА ВЫПОЛНЕНА!           ");           
            Console.WriteLine("══════════════════════════════════════════════════════════");
        }
    }
}