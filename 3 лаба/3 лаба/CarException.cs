using System;
using System.Collections.Generic;
using System.Text;

namespace _3_лаба
{
    class CarException : Exception
    {
        int ошибкаГод = 0;  // Для хранения некорректного года выпуска
        int ошибкаСкорость = 0;
        int ошибкаАварии = 0;
        double ошибкаСтоимость = 0;
        Exception стандартнаяОшибка;

        //классический стандартный
        public CarException(int x)
        {
            ошибкаГод = x;// Сохраняем неправильный год в поле класса
        }

        // Проверяем, с чем связана ошибка, и сохраняем в соответствующее поле
        public CarException(string тип, int значение)
        {
            if (тип == "скорость")
                ошибкаСкорость = значение; // Сохраняем неправильную скорость
            if (тип == "аварии")
                ошибкаАварии = значение; // Сохраняем неправильное количество аварий
        }

        //обработка ленивой функции
        public CarException(double x)
        {
            ошибкаСтоимость = x; // Сохраняем неправильную стоимость
        }

        //обработка стандартных exceptions
        public CarException(Exception x)
        {
            стандартнаяОшибка = x;// Сохраняем системную ошибку
        
        }
        
        public void obrabotka()
        {
            // Если есть стандартная ошибка (FormatException, OverflowException и т.д.)
            if (стандартнаяОшибка != null)
            {
                // Проверяем, какого типа стандартная ошибка, и выводим соответствующее сообщение
                if (стандартнаяОшибка is FormatException) // Ошибка формата: пользователь ввёл буквы вместо чисел
                    Console.WriteLine("Ошибка: Неверный формат ввода! Введите число.");
                if (стандартнаяОшибка is OverflowException)
                    Console.WriteLine("Ошибка: Число слишком большое или слишком маленькое!");
                if (стандартнаяОшибка is IndexOutOfRangeException)
                    Console.WriteLine("Ошибка: Индекс выходит за пределы диапазона!");
                if (стандартнаяОшибка is IOException)
                    Console.WriteLine("Ошибка: Проблема при чтении/записи файла!");
                return;
            }

            //обработки ошибки 
            if (ошибкаГод != 0) // Если есть ошибка года (значение не 0)
            {
                if (ошибкаГод < 1980)
                    Console.WriteLine($"Ошибка: Год выпуска {ошибкаГод} меньше 1980!");
                if (ошибкаГод > 2025)
                    Console.WriteLine($"Ошибка: Год выпуска {ошибкаГод} больше 2025!");
                return;
            }

            if (ошибкаСкорость != 0)
            {
                if (ошибкаСкорость < 60)
                    Console.WriteLine($"Ошибка: Скорость {ошибкаСкорость} км/ч меньше 60!");
                if (ошибкаСкорость > 300)
                    Console.WriteLine($"Ошибка: Скорость {ошибкаСкорость} км/ч больше 300!");
                return;
            }

            if (ошибкаАварии != 0)
            {
                if (ошибкаАварии < 0)
                    Console.WriteLine($"Ошибка: Количество аварий {ошибкаАварии} не может быть отрицательным!");
                if (ошибкаАварии > 20)
                    Console.WriteLine($"Ошибка: Количество аварий {ошибкаАварии} больше 20!");
                return;
            }

            if (ошибкаСтоимость != 0)
            {
                if (ошибкаСтоимость < 10)
                    Console.WriteLine($"Ошибка: Стоимость ремонта {ошибкаСтоимость} меньше 10 лей!");
                if (ошибкаСтоимость > 10000)
                    Console.WriteLine($"Ошибка: Стоимость ремонта {ошибкаСтоимость} больше 10000 лей!");
                return;
            }
        }
    }
}    
